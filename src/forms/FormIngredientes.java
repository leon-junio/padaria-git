    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linhaSelecionada = tblIngred.getSelectedRow();
        Ingredientes obj = listIngredientes.get(linhaSelecionada);
        txtNome.setText(obj.getNome());
        txtPeso.setText("" + obj.getPeso());
        cbxForncedor.setEnabled(true);
        cbxForncedor.setSelectedIndex(obj.getFornecedor() - 1);
        txtNome.requestFocus();
        btnSalvar.setEnabled(false);
        btnNovo.setEnabled(false);
        btnAlterar.setEnabled(true);
        txtNome.setEnabled(true);
        txtPeso.setEnabled(true);
    }//GEN-LAST:event_btnEditarActionPerformed
