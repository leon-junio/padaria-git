    /**
     * Método para atualizar pesquisa de dados sobre vendas com filtro de
     * clientes
     *
     * @param nome O nome do cliente como argumento
     */
    public void atualizarVCli(String nome) {
        ArrayList<Cliente> listc = new ArrayList<>();
        String word = "";
        String letra = "", letra2 = "";
        for (Cliente cliente : listClie) {
            for (int i = 0; i < nome.length(); i++) {
                letra = "" + cliente.getNome().charAt(i);
                letra2 = "" + nome.charAt(i);
                System.out.println(letra + "--" + letra2);
                if (letra.equalsIgnoreCase(letra2)) {
                    word += letra2;
                }
            }
            if (word.equals(nome)) {
                listc.add(cliente);
                System.out.println(word);
                System.out.println(nome);
                word = "";
            }
            word = "";
        }
        word = "";
        listVendas.clear();
        for (Cliente cliente : listc) {
            listVendas.addAll(daov.localizarNome("select * from venda where idCliente like '" + cliente.getIdCliente() + "';"));
        }
    }

