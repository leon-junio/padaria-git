    /**
     * Salvar o adm
     *
     * @param evt clique no botão
     */
    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        user = txtUser.getText();
        senha = txtSenha.getText();
        if (rbrS.isSelected()) {
            Funcionario obj = null;
            for (Funcionario f : listFunc) {
                if (f.getTipo().equals("Gerente")) {
                    obj = f;
                }
            }
            obj.setCPF(txtCpfA.getText());
            obj.setNome(txtNomea.getText());
            obj.setEndereco(txtEndA.getText());
            obj.setTipo("Gerente");
            daof.alterar(obj);
            txtCpfA.setEnabled(!true);
            txtEndA.setEnabled(!true);
            txtNomea.setEnabled(!true);
            configs.dispose();
            try {
                d.escrever(user, senha);
                JOptionPane.showMessageDialog(null, "atualizado com sucesso");

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "não atualizado com sucesso");
                System.out.println("Erros");
            }
            setVisible(true);
        } else {
            configs.dispose();
            try {
                d.escrever(user, senha);
                JOptionPane.showMessageDialog(null, "atualizado com sucesso");
            } catch (IOException ex) {
                System.out.println("Erros");
                JOptionPane.showMessageDialog(null, "não atualizado com sucesso");
            }
            setVisible(true);
        }
    }//GEN-LAST:event_saveActionPerformed
