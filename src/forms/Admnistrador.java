    private void btnRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelatorioActionPerformed
        double soma = 0;
        Date dia = new Date();
        for (Venda listVenda : listVendas) {
            soma += listVenda.getPreco();
        }
        DecimalFormat dcm = new DecimalFormat("#######.##");
        JOptionPane.showMessageDialog(null, "Relatório de Vendas \n"
                + "Total de vendas : " + listVendas.size() + "\n"
                + "Total arrecadado : " + dcm.format(soma) + "\n"
                + "Número de clientes : " + listClie.size() + "\n"
                + "Número de funcionários : " + listFunc.size() + "\n"
                + "Relatorio do dia " + dia);
    }//GEN-LAST:event_btnRelatorioActionPerformed
