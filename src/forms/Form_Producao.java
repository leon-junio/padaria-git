    /**
     * Método que salva no banco de dados os produtos produzidos
     *
     * @param evt Clique no botão produzir
     */
    private void btnProduzirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProduzirActionPerformed
        Produz obj = new Produz();
        Produto p = null;
        Funcionario f = null;
        Receita r = null;
        try{
        for (Produto listProduto : listProdutos) {
            if (listProduto.getNome().equals(cbxProd.getSelectedItem().toString())) {
                p = listProduto;
            }
        }
        for (Funcionario listFunc : listFuncionarios) {
            if (listFunc.getNome().equals(cbxFunc.getSelectedItem().toString())) {
                f = listFunc;
            }
        }
        obj.setFuncionario(f.getIdFunc());
        obj.setProduto(p.getIdProduto());
        daop.incluir(obj);
        for (Receita listReceita : listReceitas) {
            if (listReceita.getNome().equals(cbxReceita.getSelectedItem().toString())) {
                r = listReceita;
            }
        }
        Receita_Produto rp = new Receita_Produto();
        rp.setProduto(p.getIdProduto());
        rp.setReceita(r.getIdReceita());
        daorp.incluir(rp);
        for (Produto produto : daopt.getLista()) {
            if (produto.getNome().equals(p.getNome())) {
                produto.setQuantidade(produto.getQuantidade() + Integer.parseInt(spnQtd.getValue().toString()));
                daopt.alterar(produto);
            }
        }
            JOptionPane.showMessageDialog(null,"Produzido com sucesso");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Não produzido");
        }
        txtPreco.setText("Preço para produzir o produto: R$");
        txtTotal.setText("Total gasto na produção:");
        txtReceita.setText("");
        txtProd.setText("");
        spnQtd.setValue(0);
        iniciar();
    }//GEN-LAST:event_btnProduzirActionPerformed
