    /**
     * Método que atualiza o preço total da produção
     *
     * @param evt Escolha da quantidade no spinner
     */
    private void spnQtdStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spnQtdStateChanged
        DecimalFormat dcm = new DecimalFormat("#######.##");
        for (Produto produto : daopt.getLista()) {
            if (produto.getNome().equals(cbxProd.getSelectedItem().toString())) {
                txtTotal.setText("Total gasto na produção: R$ " + dcm.format(((double) produto.getPreco() * Integer.parseInt(spnQtd.getValue().toString()))));
            }
        }
    }//GEN-LAST:event_spnQtdStateChanged

