    public void atualizarProd(String nome) {
        cbxProd.removeAll();
        DefaultComboBoxModel dft = new DefaultComboBoxModel(daopt.localizarNome("select * from produto where nome like '" + nome + "%';").toArray());
        if (dft != null) {
            cbxProd.setModel(dft);
            for (Produto produto : daopt.getLista()) {
                if (produto.getNome().equals(cbxProd.getSelectedItem().toString())) {
                    txtPreco.setText("Preço para produzir o produto: R$ " + produto.getPreco());
                }
            }
        }

    }

    /**
     * Método que atualiza o combobox de receitas
     *
     * @param nome Cada nome digitado no campo de texto
     */
    public void atualizarReceita(String nome) {
        cbxReceita.removeAll();
        DefaultComboBoxModel dft = new DefaultComboBoxModel(daorc.localizarNome("select * from receita where nome like 'Receita de " + nome + "%';").toArray());
        if (dft != null) {
            cbxReceita.setModel(dft);
        }
    }

    /**
     * Método que inicia as variaveis, comboboxes
     */
    public void iniciar() {
        cbxReceita.removeAll();
        DefaultComboBoxModel dft = new DefaultComboBoxModel(daorc.getLista().toArray());
        if (dft != null) {
            cbxReceita.setModel(dft);
        }
        cbxProd.removeAll();
        DefaultComboBoxModel dft2 = new DefaultComboBoxModel(daopt.getLista().toArray());
        if (dft2 != null) {
            cbxProd.setModel(dft2);
        }
        cbxFunc.removeAll();
        DefaultComboBoxModel dft3 = new DefaultComboBoxModel(daof.getLista().toArray());
        if (dft3 != null) {
            cbxFunc.setModel(dft3);
        }

        listFuncionarios.clear();
        listProdutos.clear();
        listReceitas.clear();
        listFuncionarios.addAll(daof.getLista());
        listProdutos.addAll(daopt.getLista());
        listReceitas.addAll(daorc.getLista());

    }

