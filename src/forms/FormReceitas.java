    /**
     * Método que salva uma receita nova no banco de dados
     *
     * @param evt Clique no botão salvar
     */
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Receita obj = new Receita();
        obj.setPreco(Float.parseFloat(txtPreco.getText()));
        obj.setNome(txtNome.getText());
        Receita r = listReceitas.get(listReceitas.size() - 1);
        obj.setIdReceita(r.getIdReceita() + 1);
        obj.setDescricao(txaDescricao.getText());
        try{
        daof.incluir(obj);
        for (Ingredientes ingredientes : listSelec) {
            Usa usa = new Usa();
            usa.setReceita(obj.getIdReceita());
            usa.setIngrediente(ingredientes.getIdIngrediente());
            daou.incluir(usa);
        } JOptionPane.showMessageDialog(null,"Incluido com sucesso");
        }catch(Exception e ){
          JOptionPane.showMessageDialog(null,"Não Incluido com sucesso");  
        }
        atualizaTabela();
        limpar();
        txtNome.setEnabled(!true);
        txtPreco.setEnabled(!true);
        txaDescricao.setEnabled(false);
        btnSalvar.setEnabled(!true);
        btnNovo.setEnabled(!false);
        lstIng.setEnabled(!true);
        lstIng2.setEnabled(!true);
        btnAdd.setEnabled(!true);
        btnRemover.setEnabled(!true);
    }//GEN-LAST:event_btnSalvarActionPerformed
    /**
     * Método que alterar o valor de uma receita no banco de dados
     *
     * @param evt Clique no botão alterar
     */
    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tblReceitas.getSelectedRow();
        Receita obj = listReceitas.get(linhaSelecionada);
        obj.setPreco(Float.parseFloat(txtPreco.getText()));
        obj.setNome(txtNome.getText());
        obj.setDescricao(txaDescricao.getText());
        try{
        for (Ingredientes ingredientes : listSelec) {
            Usa usa = new Usa();
            usa.setReceita(obj.getIdReceita());
            usa.setIngrediente(ingredientes.getIdIngrediente());
            for (Usa u : daou.getLista()) {
                if (u.getReceita() == (usa.getReceita()) && u.getIngrediente() == (usa.getIngrediente())) {
                    usa.setIdUsa(u.getIdUsa());
                }
            }
            if (usa.getIdUsa() == 0) {
                daou.incluir(usa);
            } else {
                daou.alterar(usa);
            }
        }
        for (Ingredientes ingredientes : listRemove) {
            Usa usa = new Usa();
            usa.setReceita(obj.getIdReceita());
            usa.setIngrediente(ingredientes.getIdIngrediente());
            for (Usa u : daou.getLista()) {
                if (u.getReceita() == (usa.getReceita()) && u.getIngrediente() == (usa.getIngrediente())) {
                    usa.setIdUsa(u.getIdUsa());
                    daou.remover(usa);
                }
            }}
        }catch(Exception e ){
            JOptionPane.showMessageDialog(null,"Não Atualizado com sucesso");
        }
        try {
            daof.alterar(obj);
            JOptionPane.showMessageDialog(null,"Atualizado com sucesso");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Não Atualizado com sucesso");
        }
        atualizaTabela();
        limpar();
        btnSalvar.setEnabled(false);
        btnNovo.setEnabled(!false);
        btnAlterar.setEnabled(!true);
        lstIng.setEnabled(!true);
        lstIng2.setEnabled(!true);
        txaDescricao.setEnabled(false);
        txtPreco.setEnabled(false);
        txtNome.setEnabled(false);
        btnAdd.setEnabled(!true);
        btnRemover.setEnabled(!true);

    }//GEN-LAST:event_btnAlterarActionPerformed
    /**
     * Método que exclui uma receita do banco de dados
     *
     * @param evt
     */
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblReceitas.getSelectedRow();
        Receita obj = listReceitas.get(linhaSelecionada);
        ArrayList<Usa> listaUsa = new ArrayList<>();
        listaUsa.clear();
        for (Usa usa : daou.getLista()) {
            if (usa.getReceita() == obj.getIdReceita()) {
                listaUsa.add(usa);
            }
        }
        for (Usa usa : listaUsa) {
            daou.remover(usa);
        }
        daof.remover(obj);
        atualizaTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed
