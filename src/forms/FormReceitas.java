    /**
     * Método que localiza uma receita no banco de dados com base no seu nome
     *
     * @param nome Nome que for digitado no campo de texto
     */
    public void atualizarFind(String nome) {
        listReceitas.clear();
        listReceitas.addAll(daof.localizarNome("select * from receita where nome like 'receita de " + nome + "%';"));
    }

